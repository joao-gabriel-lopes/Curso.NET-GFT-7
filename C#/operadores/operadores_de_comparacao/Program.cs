//Operador IGUAL (==)

if (5 == 5)
{
    Console.WriteLine("São iguais");
}

//Operador DIFERENTE (!=)

if (5 != 3)
{
    Console.WriteLine("São diferentes");
}

//Operador MAIOR (>)

if (5 > 3)
{
    Console.WriteLine("5 é maior");
}

//Operador MENOR (<)

if (3 < 5)
{
    Console.WriteLine("3 é menor");
}

//Operador MAIOR OU IGUAL (>=)

if (6 >= 5)
{
    Console.WriteLine("6 é maior ou igual a 5");
}

//Operador MENOR OU IGUAL (<=)

if (4 <= 5)
{
    Console.WriteLine("4 é menor ou igual a 5");
}
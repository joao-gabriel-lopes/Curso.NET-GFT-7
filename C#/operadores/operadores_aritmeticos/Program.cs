//Operador SOMA (+)
int num1 = 5;
int num2 = 10;

num1 = num1 + num2;

Console.WriteLine(num1);

//Operador SUBTRAÇÃO (-)
int num3 = 5;
int num4 = 10;

num4 = num4 - num3;

Console.WriteLine(num4);

//Operador MULTIPLICAÇÃO (*)
int num5 = 5;
int num6 = 10;

num5 = num5 * num6;

Console.WriteLine(num5);

//Operador DIVISÃO (/)
int num7 = 5;
int num8 = 10;

num8 = num8 / num7;

Console.WriteLine(num8);

//Operador MÓDULO (%)
int num9 = 5;
int num10 = 10;

num10 = num10 % num9;

Console.WriteLine(num10);

//Operador INCREMENTO (++)
int num11 = 10;

num11++;

Console.WriteLine(num11);

//Operador DECREMENTO (--)
int num12 = 10;

num12--;

Console.WriteLine(num12);
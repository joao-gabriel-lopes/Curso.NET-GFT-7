NOT NULL - Garante que uma coluna não possa ter um valor NULL
NULL - Garante que uma coluna possa ter um valor NULL
IDENTITY(x, y) - Garante que a coluna se auto incremente, onde (x) é o valor inicial e (y) é a quantidade que é incrementada a cada vez
AUTO_INCREMENT - Garante que a coluna se auto incremente
UNIQUE - Garante que todos os valores de uma coluna sejam únicos
PRIMARY KEY - Chave única que identifica cada registro na tabela
FOREIGN KEY - Chave que identifica e relaciona um registro existente em outra tabela
DEFAULT 'valor'- Define um valor padrão para a coluna se nenhum valor for passado
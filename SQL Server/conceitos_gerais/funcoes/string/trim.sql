--Comando TRIM, retorna uma string sem espaços em branco no início e no final

--Selecionando o tamanho de uma string
SELECT TRIM('     abcdef     ')

--Comando RTRIM, retorna uma string sem espaços em branco no final

--Selecionando o tamanho de uma string
SELECT RTRIM('abcdef     ')

--Comando LTRIM, retorna uma string sem espaços em branco no início

--Selecionando o tamanho de uma string
SELECT LTRIM('     abcdef')
--Comando UPPER, Transforma um texto em maiúsculo

--Deixando as colunas Nome e Cor maiúsculas
SELECT UPPER(Nome) Nome, UPPER(Cor) Cor FROM Produtos



--Comando LOWER, Transforma um texto em minúsculo

--Deixando as colunas Nome e Cor minúsculas
SELECT LOWER(Nome) Nome, LOWER(Cor) Cor FROM Produtos
--Igual (=)
SELECT * FROM Clientes
WHERE Nome = 'Adam'

--Maior que (>)
SELECT * FROM Clientes
WHERE Idade > 20

--Menor que (<)
SELECT * FROM Clientes
WHERE Idade < 20

--Maior ou igual a (>=)
SELECT * FROM Clientes
WHERE Idade >= 20

--Menor ou igual a (<=)
SELECT * FROM Clientes
WHERE Idade <= 20

--Diferente (<> ou !=)
SELECT * FROM Clientes
WHERE Idade <> 20